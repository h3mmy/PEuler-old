% FIND ABUNDANT #s
afinder (X,S) ->
	T = tools:d(X),
	if T > X -> 
		store(S, X);
	true -> timeout
	end.

alist (S) ->
	receive
		{From, {store, P}} ->
			From ! {self(), ok},
			alist([P|S]);
		{From, print} ->
			io:format("~p, ",[S]),
			From ! {self(), S};
		terminate ->
			ok
	end.
	
store(Pid, P) ->
	Pid ! {self(), {store, P}},
	receive
		{Pid, Msg} -> Msg
	end.

